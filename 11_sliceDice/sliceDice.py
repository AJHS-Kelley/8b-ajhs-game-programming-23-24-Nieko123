alphabet = "abcdefghijklmnopqrstuvwxyz"
myString = "you are not real!\n"

print(alphabet[0])
print(alphabet[-1])

# Slicing
print(alphabet[0:4]) # Slice UP To second index.
# alphabet[start:stop]

# Slice from the Start
print(alphabet[:7])

# Slice to the End
print(alphabet[12:])

# Slice the whole %#$#! thing
print(alphabet[:])

# Negative Index Slicing
print(alphabet[-4:-1])

print(myString[0:18])

